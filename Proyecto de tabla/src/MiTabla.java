        arreglo[0]=txtNombre.getText();
        arreglo[1]=txtMatricula.getText();
        arreglo[2]=txtPromedio.getText();
        arreglo[3]=txtTelefono.getText();
        modeloTabla.addRow(arreglo);
        txtNombre.setText(" ");
        txtMatricula.setText(" ");
        txtPromedio.setText(" ");
        txtTelefono.setText(" ");
        
